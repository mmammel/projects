main:
  mov r7, 0
  mov [1437], 0xff10 
  mov [1337], 0  //bad lo
  mov [1737], 0  //bad hi
  mov [1537], 0  //good lo
  mov [1637], 0  //good hi
  mov [1237], 0x4528
  rand r3, 2
  mov r5, r3
  rand r3, 2
  add r3, 2
  mov r6, r3

eater:
  jmp vtravel

eatfood:
  sense r0
  cmp r0, 0xFFFF
  je oncollect
  cmp r0, 32
  jg return
  cmp r0, 1
  jl return
  cmp r0, 16
  jg food_hi
food_lo:
  mov r8, 0
  sub r0, 1
  mov r1, 1
  shl r1, r0     //r1 contains the bit id of the food
  test r1, [1337]
  jne return     //this is bad food
  test r1, [1537]
  jne do_safe_eat  //this is good food
  jmp do_eat
food_hi:
  mov r8, 1
  sub r0, 17
  mov r1, 1
  shl r1, r0     //r1 contains the bit id of the food
  test r1, [1737]
  jne return     //this is bad food
  test r1, [1637]
  jne do_safe_eat  //this is good food
do_eat:
  mov r2, 0
  cksum r2, 3599
  eat
  jns find_collection
  mov r3, 0
  cksum r3, 3599
  cmp r2, r3
  jne badfood
  jmp goodfood
do_safe_eat:
  eat
  jmp return
badfood:
  cmp r8, 0
  je lo_mark
  or [1737], r1
  jmp return
lo_mark:
  or [1337], r1
  jmp return
goodfood:
  cmp r8, 0
  je lo_mark_good
  or [1637], r1
  jmp return
lo_mark_good:
  or [1537], r1
  jmp return
oncollect:
  mov [1237], 0
  getxy r10, r11
  shl r10, 8
  or [1237], r10
  or [1237], r11
  energy r1
  cmp r1, 10000
  jl return
  div r1, 2
  release r1
return:
  ret

vtravel:
  travel r5
  jns nvtravel
  call eatfood
  jmp htravel
nvtravel:
  mov r4, r5
  call try_infect
  cmp r5, 0
  je set_south
  mov r5, 0
  jmp htravel
set_south:
  mov r5, 1
  jmp htravel
  
htravel:
  travel r6
  jns nhtravel
  call eatfood
  jmp vtravel
nhtravel:
  mov r4, r6
  call try_infect
  cmp r6, 2
  je set_west
  mov r6, 2
  jmp vtravel
set_west:
  mov r6, 3
  jmp vtravel
 
try_infect:
  mov r1, r4
  peek r1, 1437
  jns done_infect
  cmp r1, 0xff10
  je friend
  cmp r1, 0xff12
  je drone_friend
first_infect:
  cmp r7, 3
  je primary_infect
  rand r2, 30
  mult r2, 3
  mov r0, 6
  poke r4, r2
  jns done_infect
  mov r0, r2
  add r2, 1
  poke r4, r2
  jns done_infect
  add r7, 1
  jmp done_infect
primary_infect:
  mov r3, 0  //counter
  mov r2, drone
infect_loop:
  cmp r2, end_drone
  je finish_infect
  mov r0, [r2]
  poke r4, r3
  jns done_infect
  add r3, 1
  add r2, 1
  jmp infect_loop
finish_infect:
  mov r0, 0xff12
  poke r4, 1437
  mov r0, [1237]
  poke r4, 1237
  mov r0, [1337]
  poke r4, 1337
  mov r0, [1737]
  poke r4, 1737
  energy r2
  div r2, 5
  charge r4, r2
done_infect:
  ret
drone_friend:
  mov r0, [1337]
  poke r4, 1337
  mov r0, [1737]
  poke r4, 1737
  energy r2
  div r2, 5
  charge r4, r2  
  ret
friend:
  cmp [1237], 0x4528
  jne get_food_info
  mov r1, r4
  peek r1, 1237
  jns done_infect
  mov [1237], r1
get_food_info:
  mov r1, r4
  peek r1, 1337
  jns done_infect
  or r1, [1337]
  mov [1337], r1
  mov r1, r4
  peek r1, 1737
  jns done_infect
  or r1, [1737]
  mov [1737], r1
  ret

find_collection:
  cmp [1237], 0x4528
  je return
  getxy r0, r1
  mov r10, 0xFF00
  and r10, [1237]
  shr r10, 8
  mov r11, 0x00FF
  and r11, [1237]
hfind:
  mov r2, 1
  cmp r10, r0
  je vfind
  jl wfind
  jg efind
wfind:    
  travel 3
  jns fstuck
  sub r0, 1
  jmp hfind
efind:
  travel 2
  jns fstuck
  add r0, 1
  jmp hfind
vfind:
  mov r2, 0
  cmp r11, r1
  je check_found
  jl nfind
  jg sfind
sfind:
  travel 1
  jns fstuck
  add r1, 1
  jmp vfind
nfind:
  travel 0
  jns fstuck
  sub r1, 1
  jmp vfind
check_found:
  getxy r0, r1
  cmp r10, r0
  jne hfind
  cmp r11, r1
  jne vfind
  release 10000
  jmp return
fstuck:
  rand r2, 4
  travel r2
  rand r2, 4
  travel r2
  rand r2, 4
  travel r2
  rand r2, 4
  travel r2
